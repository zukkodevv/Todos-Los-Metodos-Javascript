//** Para este ejemplo vamos a ordenar un array constante con varios strings dentro

const palabras = ["pepito", "juanita", "fausticia", "aaron", "benja"];
// Definimos el array con 5 strings dentro
palabras.sort();
// Usamos el método sort() para ordenarlos alfabeticamente;
console.log(palabras); // ["aaron", "benja", "fausticia", "juanita", "pepito"]

// Cómo ven esta es la forma más sencilla de utilizar sort()
